"""Write a python program to create a function that checks whether a passed string is
palindrome or not."""

def fun():
  a=str(input("enter a string "))
  b=a[-1::-1]
  
  if a==b:
    print("string is palindrome")
  else:
    print("string is not palindrome")  
  
  
  
  
  

  

fun()  